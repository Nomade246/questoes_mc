function checaMaior(Numero1,Numero2){
    if (Numero1 > Numero2){
        console.log (Numero1+ " é maior que " +Numero2)
    }
    if (Numero1 < Numero2){
        console.log (Numero1+ " é menor que " +Numero2)
    }
    if (Numero1 == Numero2){
        console.log (Numero1+ " igual " +Numero2)
    }
}
checaMaior(3,4);